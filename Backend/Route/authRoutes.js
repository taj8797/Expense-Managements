const express = require("express")
const router = express.Router()
const middlewareAuth = require("../middleware/userAuth")
const {handleUserSignup , sendEmail,handleOtpVerification ,handleUserLogin ,verifyOtpEmail ,reset_Password,getUserDetail } =require("../Controller/authController")

// router.use(middlewareAuth)


// ===========sign up  user==========================

router.post('/signUp',handleUserSignup)


// verify user with otp 

router.post('/verifyOtp',handleOtpVerification)


// ===============================login user =============================

router.post('/loginUser',handleUserLogin)


//==================forgot password =============================

router.post('/forgotPassword',sendEmail)

//==================verify otp email =============================

router.post('/verify_Otp_Email',verifyOtpEmail)

//==================reset password =============================

router.post('/reset_Password/:id',reset_Password)


//==============get user by id ========================

router.get('/getUserById/:id',middlewareAuth,getUserDetail)








